    private boolean validar() {

        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo Nome");
            return true;
        } else if (txtEnderaco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo Endereço");
            return true;
        } else if (txtNumero.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo Numero");
            return true;
        } else if (txtBairro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo Bairro");
            return true;
        } else if (txtCPF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo CPF/CNPJ");
            return true;
        } else if (txtTelefone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo Telefone");
            return true;
        } else if (txtCelular.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo Celular");
            return true;
        } else if (txtCEP.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preencher o campo CEP");
            return true;
        }

        return false;

    }

