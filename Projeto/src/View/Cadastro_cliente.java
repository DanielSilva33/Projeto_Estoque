    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validar()) {
            return;
        }

        Cliente objCliente = new Cliente();
        ClienteDAO clienteBD = new ClienteDAO();
        clienteBD.inserir_Cliente(objCliente);

        objCliente.setNome(txtNome.getText());
        objCliente.setBairro(txtBairro.getText());
        objCliente.setEndereco(txtEnderaco.getText());
        objCliente.setCpf(txtCPF.getText());
        objCliente.setCelular(txtCelular.getText());
        objCliente.setCep(txtCEP.getText());
        objCliente.setNumero(txtNumero.getText());
        objCliente.setTelefone(txtTelefone.getText());

        if (clienteBD.inserir_Cliente(objCliente)) {
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!!!");
            clearForm();
        } else {
            JOptionPane.showMessageDialog(null, "Erro em adicionar o cliente!!!");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

